public class Usuario {

    private int id;
    private String cpf;
    private String senha;
    private String perfil;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getCpf() {
        return cpf;
    }

    public void setCpf(String cpf) {
        this.cpf = cpf;
    }

    public String getSenha() {
        return senha;
    }

    public void setSenha(String senha) {
        this.senha = senha;
    }

    public String getPerfil() {
        return perfil;
    }

    public void setPerfil(String perfil) {
        this.perfil = perfil;
    }

    public Usuario(int id, String cpf, String senha, String perfil) {
        this.id = id;
        this.cpf = cpf;
        this.senha = senha;
        this.perfil = perfil;
    }

}
